def compute_bin_loss(output, target, mask):
    mask = mask.expand_as(output)
    output = output * mask.float()
    return F.cross_entropy(output, target, reduction='elementwise_mean')

def compute_rot_loss(output, target_bin, target_res, mask):
    # output: (B, 128, 8) [bin1_cls[0], bin1_cls[1], bin1_sin, bin1_cos, 
    #                 bin2_cls[0], bin2_cls[1], bin2_sin, bin2_cos]
    # target_bin: (B, 128, 2) [bin1_cls, bin2_cls]
    # target_res: (B, 128, 2) [bin1_res, bin2_res]
    # mask: (B, 128, 1)
    # import pdb; pdb.set_trace()
    output = output.view(-1, 8)
    target_bin = target_bin.view(-1, 2)
    target_res = target_res.view(-1, 2)
    mask = mask.view(-1, 1)
    loss_bin1 = compute_bin_loss(output[:, 0:2], target_bin[:, 0], mask)
    loss_bin2 = compute_bin_loss(output[:, 4:6], target_bin[:, 1], mask)
    loss_res = torch.zeros_like(loss_bin1)
    if target_bin[:, 0].nonzero().shape[0] > 0:
        idx1 = target_bin[:, 0].nonzero()[:, 0]
        valid_output1 = torch.index_select(output, 0, idx1.long())
        valid_target_res1 = torch.index_select(target_res, 0, idx1.long())
        loss_sin1 = compute_res_loss(
          valid_output1[:, 2], torch.sin(valid_target_res1[:, 0]))
        loss_cos1 = compute_res_loss(
          valid_output1[:, 3], torch.cos(valid_target_res1[:, 0]))
        loss_res += loss_sin1 + loss_cos1
    if target_bin[:, 1].nonzero().shape[0] > 0:
        idx2 = target_bin[:, 1].nonzero()[:, 0]
        valid_output2 = torch.index_select(output, 0, idx2.long())
        valid_target_res2 = torch.index_select(target_res, 0, idx2.long())
        loss_sin2 = compute_res_loss(
          valid_output2[:, 6], torch.sin(valid_target_res2[:, 1]))
        loss_cos2 = compute_res_loss(
          valid_output2[:, 7], torch.cos(valid_target_res2[:, 1]))
        loss_res += loss_sin2 + loss_cos2
    return loss_bin1 + loss_bin2 + loss_res


class TripletLoss(nn.Module):
    """Triplet loss with hard positive/negative mining.
    Reference:
    Hermans et al. In Defense of the Triplet Loss for Person Re-Identification. arXiv:1703.07737.
    Code imported from https://github.com/Cysu/open-reid/blob/master/reid/loss/triplet.py.
    Args:
        margin (float): margin for triplet.
    """

    def __init__(self, margin=0.3, mutual_flag=False):
        super(TripletLoss, self).__init__()
        self.margin = margin
        self.ranking_loss = nn.MarginRankingLoss(margin=margin)
        self.mutual = mutual_flag

    def forward(self, inputs, targets):
        """
        Args:
            inputs: feature matrix with shape (batch_size, feat_dim)
            targets: ground truth labels with shape (num_classes)
        """
        n = inputs.size(0)
        # inputs = 1. * inputs / (torch.norm(inputs, 2, dim=-1, keepdim=True).expand_as(inputs) + 1e-12)
        # Compute pairwise distance, replace by the official when merged
        dist = torch.pow(inputs, 2).sum(dim=1, keepdim=True).expand(n, n)
        dist = dist + dist.t()
        dist.addmm_(1, -2, inputs, inputs.t())
        dist = dist.clamp(min=1e-12).sqrt()  # for numerical stability
        # For each anchor, find the hardest positive and negative
        mask = targets.expand(n, n).eq(targets.expand(n, n).t())
        dist_ap, dist_an = [], []
        for i in range(n):
            dist_ap.append(dist[i][mask[i]].max().unsqueeze(0))
            dist_an.append(dist[i][mask[i] == 0].min().unsqueeze(0))
        dist_ap = torch.cat(dist_ap)
        dist_an = torch.cat(dist_an)
        # Compute ranking hinge loss
        y = torch.ones_like(dist_an)
        loss = self.ranking_loss(dist_an, dist_ap, y)
        if self.mutual:
            return loss, dist
        return loss


class GroupSoftmaxLoss():
    def __init__(self,
                 gs_config=None,
                 *args,
                 **kwargs):
    
        # GPU
        torch.cuda.set_device(0) 

        # group
        self.group_num = gs_config["group_num"]

        self.label2binlabel = torch.load(gs_config["label2binlabel"]).cuda()
        self.pred_slice = torch.load(gs_config["pred_slice"]).cuda()

        self.MOT17_train_half = False
        self.MOT20_train_half = False
        
        if "/MOT17_train_half_04/" in gs_config["id_newid"]:
          self.MOT17_train_half = True

        if "/MOT17_train_half_strengthened_before_correction/" in gs_config["id_newid"]:
          self.MOT17_train_half = True

        if "/MOT17_train_half_strengthened_after_correction/" in gs_config["id_newid"]:
          self.MOT17_train_half = True

        if "/MOT17_train_half_after_gs_10_100_correction/" in gs_config["id_newid"]: 
           self.MOT17_train_half = True

        # ----------------------------------------------------------------------------- # 
        # MOT20 train half
        if "/MOT20_train_LastQuarter_DCVDA_GS_correction/" in gs_config["id_newid"]: 
           self.MOT20_train_half = True

        if "/MOT20_train_LastTwoQuarters_DCVDA_GS_correction/" in gs_config["id_newid"]: 
           self.MOT20_train_half = True

        if "/MOT20_train_LastThreeQuarters_DCVDA_GS_correction/" in gs_config["id_newid"]: 
           self.MOT20_train_half = True

        self.id_newid = []
        f = open(gs_config["id_newid"])   
        line = f.readline()               
        while line: 
            self.id_newid.append(int(line.split(",")[0])) 
            line = f.readline()

        self.id_newid = torch.IntTensor(self.id_newid).cuda() # Convert to tensor, CPU --> GPU

    def _remap_labels(self, labels):
        new_labels = []
        group_num = self.label2binlabel.shape[0]
        for i in range(group_num): 
            mapping = self.label2binlabel[i] 
            new_bin_label = mapping[labels] 

            new_labels.append(new_bin_label)

        return new_labels

    def _slice_preds(self, cls_score):

        new_preds = []
        group_num = self.pred_slice.shape[0]
        for i in range(group_num):
            start = self.pred_slice[i, 0]
            length = self.pred_slice[i, 1]
            sliced_pred = cls_score.narrow(1, start, length)
            new_preds.append(sliced_pred)

        return new_preds

    def loss(self,
             IDLoss,
             cls_score,
             labels):
        
        losses = 0
        """
        # training
        Args:
            IDLoss: loss function
            cls_score: 
            labels: 
        """
        if cls_score is not None:
          if True == self.MOT17_train_half or True == self.MOT20_train_half:
            for i,label in enumerate(labels): # [1,xxx]
              indices = torch.nonzero(self.id_newid == label) 
              labels[i] = indices + torch.tensor(1) 

          # Original label_weights is 1 for each roi.
          new_labels = self._remap_labels(labels)
          new_preds  = self._slice_preds(cls_score)

          group_num = self.group_num
          
          for i in range(group_num):
            loss = IDLoss(new_preds[i], new_labels[i].long()) # .cuda()ï¼šgpu calculate
            losses += loss * 1/group_num

        return losses



